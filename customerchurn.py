import streamlit as st
import pandas as pd
import joblib

# Load model and feature list
model = joblib.load("churn_model.pkl")
features = joblib.load("model_features.pkl")

st.set_page_config(page_title="Customer Churn Predictor", layout="centered")

st.title("📉 Customer Churn Prediction Tool")
st.write("Predict churn risk for a customer using business data.")

# ------------------------
# User Inputs
# ------------------------
tenure = st.slider("Tenure (months)", 0, 72, 12)
monthly_charges = st.slider("Monthly Charges", 20.0, 120.0, 70.0)
total_charges = tenure * monthly_charges

contract = st.selectbox(
    "Contract Type",
    ["Month-to-month", "One year", "Two year"]
)

internet_service = st.selectbox(
    "Internet Service",
    ["DSL", "Fiber optic", "No"]
)

payment_method = st.selectbox(
    "Payment Method",
    ["Electronic check", "Mailed check", "Bank transfer", "Credit card"]
)

tech_support = st.selectbox("Tech Support", ["Yes", "No"])
paperless_billing = st.selectbox("Paperless Billing", ["Yes", "No"])

# ------------------------
# Build input dataframe
# ------------------------
input_dict = {
    "tenure": tenure,
    "MonthlyCharges": monthly_charges,
    "TotalCharges": total_charges,
    "TechSupport": 1 if tech_support == "Yes" else 0,
    "PaperlessBilling": 1 if paperless_billing == "Yes" else 0,
}

# One-hot fields (must match training)
for col in features:
    if col not in input_dict:
        input_dict[col] = 0

# Handle categorical encodings
if contract != "Month-to-month":
    input_dict[f"Contract_{contract}"] = 1

if internet_service != "DSL":
    input_dict[f"InternetService_{internet_service}"] = 1

if payment_method != "Electronic check":
    input_dict[f"PaymentMethod_{payment_method}"] = 1

input_df = pd.DataFrame([input_dict])[features]

# ------------------------
# Prediction
# ------------------------
if st.button("Predict Churn Risk"):
    prob = model.predict_proba(input_df)[0][1]

    st.subheader("📊 Prediction Result")
    st.write(f"**Churn Probability:** {prob:.2%}")

    if prob >= 0.7:
        st.error("🔴 High Churn Risk")
        st.write("**Recommended Action:** Immediate retention offer or proactive support.")
    elif prob >= 0.4:
        st.warning("🟠 Medium Churn Risk")
        st.write("**Recommended Action:** Engagement campaigns or value-added services.")
    else:
        st.success("🟢 Low Churn Risk")
        st.write("**Recommended Action:** Maintain relationship and upsell opportunities.")

